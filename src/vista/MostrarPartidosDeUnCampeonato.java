    private void btn_mostrar_campeonatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrar_campeonatosActionPerformed
        int id_campeonato;
        Registro reg = new Registro();
        DefaultTableModel modelo = (DefaultTableModel) this.jTable1.getModel();
        modelo.setRowCount(0);
        List<Campeonato> lista = reg.listarCampeonatos();
        for (Campeonato campeonato : lista) {
            id_campeonato = campeonato.getId_campeonato();
            modelo.addRow(new Object []{id_campeonato});
        }
        
    }//GEN-LAST:event_btn_mostrar_campeonatosActionPerformed

    private void btn_mostrar_partidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrar_partidosActionPerformed
        int id_campeonato_txt = Integer.parseInt(this.txt_id_campeonato.getText());
        int id_partido,duracion,id_arbitro,id_campeonato;
        String cancha;
        Date fecha;
        Registro reg = new Registro();
        DefaultTableModel modelo = (DefaultTableModel) this.jTable2.getModel();
        modelo.setRowCount(0);
        List<Partido> lista = reg.listarPartidos(id_campeonato_txt);
        for (Partido partido : lista) {
            id_partido = partido.getId_partido();
            duracion = partido.getDuracion_min();
            id_arbitro = partido.getId_arbitro();
            id_campeonato = partido.getId_campeonato();
            cancha = partido.getCancha();
            fecha = partido.getFecha();
            modelo.addRow(new Object []{id_partido,duracion,id_arbitro,id_campeonato,cancha,fecha});
        }
        
        
    }//GEN-LAST:event_btn_mostrar_partidosActionPerformed

